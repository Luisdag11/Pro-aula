    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        Collection <Visitante> datos = Visitante.visitantesBD.values();
        ArrayList<Visitante> ListaVisitantes = new ArrayList(datos);
        String columnas [] = {"Nombre", "Telefono", "Documento", "Email", "Ocupaci√≥n"};
        int totalVisitantes = ListaVisitantes.size();
        String filas[][] = new String[totalVisitantes][columnas.length];
        for(int i =0; i < ListaVisitantes.size(); i++){
            Visitante vi = ListaVisitantes.get(i);
            filas [i][0] = vi.nombre;
            filas [i][1] = vi.telefono;
            filas [i][2] = vi.documento;
            filas [i][3] = vi.email;
            filas [i][4] = vi.ocupacion;
        }
        DefaultTableModel datosTabla = new DefaultTableModel();
        datosTabla.setDataVector(filas, columnas);
        tablaUsuarios.setModel(datosTabla);
        txtTotal.setText(totalVisitantes+"");
    }//GEN-LAST:event_formWindowOpened

